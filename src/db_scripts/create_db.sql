CREATE TABLE IF NOT EXISTS PLAYER_REFERENCE_TABLE (
    MASTER_PLAYER_ID int NOT NULL PRIMARY KEY AUTO INCREMENT
    YAHOO_PLAYER_ID int NOT NULL FOREIGN KEY REFERENCES YAHOO_PLAYER_TABLE(YAHOO_PLAYER_ID)
    NBA_PLAYER_ID int NOT NULL FOREIGN KEY REFERENCES NBA_PLAYER_TABLE(NBA_PLAYER_ID)
    DARKO_PLAYER_ID int NOT NULL FOREIGN KEY REFERENCES DARKO_PLAYER_TABLE(SEASON_ID)
);


CREATE TABLE IF NOT EXISTS YAHOO_PLAYER_TABLE (
    YAHOO_PLAYER_ID int NOT NULL PRIMARY KEY AUTO INCREMENT
    ,YAHOO_PLAYER_NAME str NOT NULL
    ,PG_ELIGIBLE boolean NOT NULL
    ,SG_ELIGIBLE boolean NOT NULL
    ,PF_ELIGIBLE boolean NOT NULL
    ,SF_ELIGIBLE boolean NOT NULL
    ,C_ELIGIBLE boolean NOT NULL
    ,IS_ACTIVE boolean NOT NULL
    ,IS_INJURED boolean NOT NULL
    ,UPDATE_DATE date NOT NULL
);

