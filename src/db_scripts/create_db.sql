    ,FREE_THROWS_MADE int NOT NULL 
    ,THREES int NOT NULL 
    ,POINTS int NOT NULL 
    ,REBOUNDS int NOT NULL 
    ,ASSISTS int NOT NULL 
    ,STEALS int NOT NULL 
    ,BLOCKS int NOT NULL 
    ,TURNOVERS int NOT NULL 
    ,MINUTES_PLAYED Decimal(6,2) NOT NULL 
    ,UPDATE_DATE date NOT NULL
);

